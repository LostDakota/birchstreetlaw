        <p>
          Rebekah Zinn is a retired Washington State Superior Court Commissioner with 18 years of 
          experience in trial and appellate courts. Her deep expertise spans multiple areas of law, 
          and she brings a practical, grounded understanding of what is truly at stake when a judicial 
          officer decides a case or a matter goes to trial.
        </p>
        <p>
          Attorney Rebekah Zinn's experience as a Court Commissioner (retired) enables her to help 
          parties resolve disputes outside of court as a mediator by facilitating open dialogue and 
          finding fair, mutually agreeable solutions. As a mediator, Ms. Zinn's role is to be neutral — 
          to help parties reach their own agreements. She facilitates civil, fair conversations where 
          all voices can be heard in a safe environment.
        </p>
        <p>
          She will only share her experience and understanding when the parties want that information, 
          and solely as background information to help reach resolutions — not as legal advice intended 
          to sway any decision. The parties can have their own lawyers present and, if they do, Ms. Zinn 
          highly encourages the parties to listen to their lawyer's advice.
        </p>
        <p>
          Ms. Zinn believes mediation can be transformative. By resolving disputes outside of court, 
          parties often achieve outcomes that are more creative, more lasting, and less costly — 
          financially and emotionally — than going to trial.
        </p>
